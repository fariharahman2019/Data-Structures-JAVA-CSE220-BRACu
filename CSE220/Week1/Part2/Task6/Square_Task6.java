public class Square_Task6
{
    public double height;
    public double width;
    public double area;
    
    public double getHeight()
    {
        return height;
    }
    public double getWidth()
    {
        return width;
    }
    public double getArea()
    {
        area = height*width;
        return area;
    }
    public void setHeight(double n)
    {
        height = n;
    }
    public void setWidth(double n)
    {
        width = n;
    }
    
}